# 주석(comment) : 프로그램 실행과는 상관없이 설명을 담는 영역
## 스크립트(script) : 여러 줄의 명령어를 입력하여 실행하는 작업영역
## Enter : 개행(줄바꿈)
## 실행(run) - 한 줄 실행 : Ctrl + Enter
## 여러 줄 실행 : 영역지정 후 Ctrl + Enter
## 스크립트 창 전체 실행 : Ctrl + Shift + Enter

